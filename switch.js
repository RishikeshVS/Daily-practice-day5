var color="blue";
switch(color)
{
    case "red":
        console.log("color is green");
        break;
    case "black":
        console.log("color is black");
        break;
    case "blue":
        console.log("color is blue");
        break;
    case "white":
        console.log("color is white");
        break;
    default:
        console.log("error!!no color found");
}
